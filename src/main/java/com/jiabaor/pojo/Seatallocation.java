package com.jiabaor.pojo;

public class Seatallocation {
	// 用户身份证
	private String userIdCard;
	// 座位编号
	private String seatSerialNumber;
	// 时间
	private String createTime;

	public String getUserIdCard() {
		return userIdCard;
	}

	public void setUserIdCard(String userIdCard) {
		this.userIdCard = userIdCard;
	}

	public String getSeatSerialNumber() {
		return seatSerialNumber;
	}

	public void setSeatSerialNumber(String seatSerialNumber) {
		this.seatSerialNumber = seatSerialNumber;
	}

	public String getCreateTime() {
		return createTime;
	}

	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}

}
